package org.gsc.medha.dto.notification;

public class DateTime {
    private String fallback_value;
    private Integer day_of_week;
    private Integer day_of_month;
    private Integer year;
    private Integer month;
    private Integer hour;
    private Integer minute;
    private Integer timestamp;
	public String getFallback_value() {
		return fallback_value;
	}
	public void setFallback_value(String fallback_value) {
		this.fallback_value = fallback_value;
	}
	public Integer getDay_of_week() {
		return day_of_week;
	}
	public void setDay_of_week(Integer day_of_week) {
		this.day_of_week = day_of_week;
	}
	public Integer getDay_of_month() {
		return day_of_month;
	}
	public void setDay_of_month(Integer day_of_month) {
		this.day_of_month = day_of_month;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Integer getHour() {
		return hour;
	}
	public void setHour(Integer hour) {
		this.hour = hour;
	}
	public Integer getMinute() {
		return minute;
	}
	public void setMinute(Integer minute) {
		this.minute = minute;
	}
	public Integer getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Integer timestamp) {
		this.timestamp = timestamp;
	}

    
}
